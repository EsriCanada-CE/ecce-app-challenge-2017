<h1>Mission</h1>
<p>Sustainability is more important now than it has ever been, our future depends on it. Three of the biggest issues facing humanity now are fossil fuel depletion, climate change and higher energy and water costs. But the good news is change is possible and it’s easier now than ever for residents of the Halifax Regional Municipality to reduce their net impact on the ecosystem with the Halifax Sustainable Transit App! 
Our app’s goal is to make it easier for people to access the vital resources they need when it comes to sustainable transportation decisions, as well as giving them the opportunity to contribute to the growth and propagation of their local transit data. Education plays a huge role in the steps to change - providing the public with easy access to intuitive utilities and applications puts this information at their fingertips.</p>
</div>

