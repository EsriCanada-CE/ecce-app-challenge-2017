<h1>Characteristics</h1>
<p>

<ul>
<li>View how close your building/location is to a bus station. The building data is coded to show variance in distance to public transit. This can be useful for looking up how long it may take you to get to a bus stop. It would be helpful for real-estate agencies when gauging variables that determine asking prices.

<li>Locations of Park n' Ride lots are available on this app which include detailed information about each spot such as cost, availability and directions to each lot. 

<li>Halifax has a bike-friendly certification that began in late 2015 - companies can apply for said certification based on the bike-friendly practices they provide. Users can look at locations that have already been added to the map or add their own.

<li>If users want to see how much money they are saving by taking the bus, riding their bike or carpooling they can use the app to calculate how much a trip would have cost them based on their mileage, gas prices and distance.

<li>The app incorporates a live feed to the HFXtransit twitter account, allowing them to quickly see if any posts have been made regarding cancellations or delays. 

</ul>
